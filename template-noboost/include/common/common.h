#ifndef _COMMON_H_
#define _COMMON_H_

/* 基本数据类型 */
typedef    void                            VD;                /**<    void型                                    */
typedef    unsigned long                   U4;                /**<    32bit无符号                               */
typedef    unsigned short                  U2;                /**<    16bit无符号                               */
typedef    unsigned char                   U1;                /**<    8bit无符号                                */
typedef    signed long                     I4;                /**<    32bit有符号                               */
typedef    signed short                    I2;                /**<    16bit有符号                               */
typedef    signed char                     I1;                /**<    8bit有符号                                */
typedef    bool                            BOOL;              /**<    bool型                                    */
typedef    long                            ISIZE;             /**<    有符号长度类型                            */
typedef    unsigned long                   USIZE;             /**<    无符号长度类型                            */

#define TRUE true
#define FALSE false

#endif /* _COMMON_H_ */
